proc calculate(self: GradientConicAngle, x, y, d: int): int =
  var r = arctan2(y.float64, x.float64)

  if r < 0:
    result = abs(1600 - trunc(abs(r) * d.float64 / pi * 0.5).int)
  else:
    result = trunc(r * d.float64 / pi * 0.5).int

