proc renderContour[GradFunc, T2, TR](app: App, gradFunc: var GradFunc, t2: var T2, D1, D2: float64, gmt: var TR) =
  if app.reflect.status():
    var gradRefl = initGradientReflectAdaptor(gradFunc)
    app.renderContourAux(gradRefl, t2, D1, D2, gmt)
  else:
    app.renderContourAux(gradFunc, t2, D1, D2, gmt)

proc performRendering[VertexSource](app: App, vs: var VertexSource, contour: var PathStorage) =
  let bb = boundingRectD(vs, 0)
  var scale = (app.width() - 120.0) / (bb.x2 - bb.x1)

  if scale > (app.height() - 120.0) / (bb.y2 - bb.y1):
    scale = (app.height() - 120.0) / (bb.y2 - bb.y1)

  var mtx = initTransAffine()
  mtx.translate(-bb.x1, -bb.y1)
  mtx.scale(scale, scale)

  var
    T1   = initConvTransform(vs, mtx)
    tat  = transAffineTranslation(100, 105)
    path = initPathStorage()

  # Init perspective control
  var
    px1 = bb.x1
    py1 = bb.y1
    px2 = bb.x2
    py2 = bb.y2

  mtx.transform(px1, py1)
  mtx.transform(px2, py2)

  if app.mInit:
    app.persp.xn(0) = px1
    app.persp.yn(0) = py1
    app.persp.xn(1) = px2
    app.persp.yn(1) = py1
    app.persp.xn(2) = px2
    app.persp.yn(2) = py2
    app.persp.xn(3) = px1
    app.persp.yn(3) = py2
  
    tat.transform(app.persp.xn(0), app.persp.yn(0))
    tat.transform(app.persp.xn(1), app.persp.yn(1))
    tat.transform(app.persp.xn(2), app.persp.yn(2))
    tat.transform(app.persp.xn(3), app.persp.yn(3))
    app.mInit = false

  # Add Perspective Transformation
  var
    trpp = initTransPerspective(px1, py1, px2, py2, app.persp.polygon())
    trpg = initTransPerspective(app.persp.polygon(), px1, py1, px2, py2)
    T2   = initConvTransform(T1, trpp)

  #if not trpp.isValid(): 
  #  echo "PP" 
  #  return
  #if not trpg.isValid(): 
  #  echo "PG"
  #  return

  path.joinPath(T1)

  var
    D1 = 0.0
    D2 = 100.0

  case app.gradient.curItem()
  of 0, 1: # contour
    var gradCont = initGradientContour()
    gradCont.frame(0)
    gradCont.d1(app.contour[0].value())
    gradCont.d2(app.contour[1].value())

    if app.gradient.curItem() == 0:
      # contour
      gradCont.contourCreate(contour)
    else:
      # auto contour
      gradCont.contourCreate(path)

    D1 = app.sliderD[0].value()
    D2 = app.sliderD[1].value()
    app.renderContour(gradCont, T2, D1, D2, trpg)
  of 2: # assymetric conic
    var gradCona = initGradientConicAngle()
    var gmt = transAffineTranslation(270, 300)
    gmt.invert()
    app.renderContour(gradCona, T2, D1, D2, gmt)
  else:
    app.ras.reset()
    app.ras.addPath(T2)
    var renScan = initRendererScanLineAASolid(app.rb)

    renScan.color(initRgba(0, 0.6, 0, 0.5))
    renderScanLines(app.ras, app.sl, renScan)

  if app.stroke.status():
    var
      gradImag = initGradientImage(PixFmtRgba32)
      bitmap = cast[ptr uint8](gradImag.imageCreate(64, 64))

    if bitmap != nil:
      for i in 0.. <Puzzle.len:
        bitmap[i] = Puzzle[i]

      var stroke = initConvStroke(T2)
      stroke.width(10.0)

      var
        spanInterpolator = initSpanInterpolatorLinear(trpg)
        spanAllocator = initSpanAllocator[Rgba8]()
        sg  = initSpanGradient(spanInterpolator, gradImag, gradImag.colorFunction(), D1, D2)
        ren = initRendererScanLineAA(app.rb, spanAllocator, sg)

      app.ras.reset()
      app.ras.addPath(stroke)
      renderScanLines(app.ras, app.sl, ren)

proc render(app: App) =
  case app.polygons.curItem()
  of 0: # Simple path
    app.performRendering(app.star, app.star)

  of 1: # Great Britain
    var path = initPathStorage()
    path.makeGBPoly()
    app.performRendering(path, app.star)

  of 2: # Spiral
    var
      spiral = initSpiral(0.0, 0.0, 10.0, 150.0, 30.0, 0.0)
      stroke = initConvStroke(spiral)

    stroke.width(22.0)
    app.performRendering(stroke, app.star)

  of 3: # Glyph
    var curve = initConvCurve(app.glyph)
    curve.approximationScale(10.0)
    app.performRendering(curve, app.star)

  else:
    discard
    
